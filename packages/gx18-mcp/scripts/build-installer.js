// Generates Inno Setup script and compiles it into a Windows installer
// Requires Inno Setup 6+ installed (winget install JRSoftware.InnoSetup)
'use strict'

const { execSync } = require('child_process')
const fs = require('fs')
const path = require('path')

const root = path.join(__dirname, '..')
const { version } = require('../package.json')

const releaseDir  = path.join(root, 'release')
const exePath     = path.join(releaseDir, 'GeneXusAIToolkit.exe')
const coreExePath = path.join(releaseDir, 'GeneXusAIToolkit.core.exe')
const workerDir   = path.join(releaseDir, 'worker')
const iconPath    = path.join(root, 'assets', 'icon.ico')
const issPath     = path.join(releaseDir, 'installer.iss')
const outputName  = `GeneXusAIToolkit-${version}-setup`

if (!fs.existsSync(exePath)) {
  console.error('ERROR: release/GeneXusAIToolkit.exe not found. Run build:exe first.')
  process.exit(1)
}
if (!fs.existsSync(coreExePath)) {
  console.error('ERROR: release/GeneXusAIToolkit.core.exe not found. Run build:exe first.')
  process.exit(1)
}

const iss = `; GeneXus AI Toolkit — Inno Setup script (auto-generated by build-installer.js)
#define AppName      "GeneXus AI Toolkit"
#define AppVersion   "${version}"
#define AppPublisher "GeneXus AI Toolkit"
#define AppURL       "https://github.com/lucaskarsten/genexus-ai-toolkit"
#define ExeName      "GeneXusAIToolkit.exe"

[Setup]
AppId={{E3A7C2B4-91D0-4F5A-8E3C-2B1F9D8A4E67}
AppName={#AppName}
AppVersion={#AppVersion}
AppPublisher={#AppPublisher}
AppPublisherURL={#AppURL}
AppSupportURL={#AppURL}
AppUpdatesURL={#AppURL}
DefaultDirName={autopf}\\{#AppName}
DefaultGroupName={#AppName}
AllowNoIcons=yes
SetupIconFile=${iconPath.replace(/\\/g, '\\\\')}
UninstallDisplayIcon={app}\\{#ExeName}
Compression=lzma2/ultra64
SolidCompression=yes
WizardStyle=modern
OutputDir=${releaseDir.replace(/\\/g, '\\\\')}
OutputBaseFilename=${outputName}
VersionInfoVersion={#AppVersion}
VersionInfoCompany={#AppPublisher}
VersionInfoDescription={#AppName} Setup

[Languages]
Name: "brazilianportuguese"; MessagesFile: "compiler:Languages\\BrazilianPortuguese.isl"
Name: "english"; MessagesFile: "compiler:Default.isl"

[Tasks]
Name: "desktopicon"; Description: "{cm:CreateDesktopIcon}"; GroupDescription: "{cm:AdditionalIcons}"; Flags: unchecked

[Files]
Source: "${exePath.replace(/\\/g, '\\\\')}"; DestDir: "{app}"; Flags: ignoreversion
Source: "${coreExePath.replace(/\\/g, '\\\\')}"; DestDir: "{app}"; Flags: ignoreversion
Source: "${workerDir.replace(/\\/g, '\\\\')}\\*"; DestDir: "{app}\\worker"; Flags: ignoreversion recursesubdirs createallsubdirs
Source: "${iconPath.replace(/\\/g, '\\\\')}"; DestDir: "{app}"; Flags: ignoreversion

[Icons]
Name: "{group}\\{#AppName}"; Filename: "{app}\\{#ExeName}"; IconFilename: "{app}\\icon.ico"
Name: "{group}\\{cm:UninstallProgram,{#AppName}}"; Filename: "{uninstallexe}"
Name: "{autodesktop}\\{#AppName}"; Filename: "{app}\\{#ExeName}"; IconFilename: "{app}\\icon.ico"; Tasks: desktopicon

[Run]
Filename: "{app}\\{#ExeName}"; Description: "{cm:LaunchProgram,{#AppName}}"; Flags: nowait postinstall skipifsilent
`

fs.writeFileSync(issPath, iss, 'utf8')
console.log('Generated:', issPath)

// Find iscc.exe
const isccPaths = [
  'C:\\Program Files (x86)\\Inno Setup 6\\ISCC.exe',
  'C:\\Program Files\\Inno Setup 6\\ISCC.exe',
  `${process.env.LOCALAPPDATA}\\Programs\\Inno Setup 6\\ISCC.exe`,
]
const iscc = isccPaths.find(p => fs.existsSync(p))
if (!iscc) {
  console.error('ERROR: ISCC.exe not found. Install Inno Setup 6: winget install JRSoftware.InnoSetup')
  process.exit(1)
}

console.log('Compiling installer...')
execSync(`"${iscc}" "${issPath}"`, { stdio: 'inherit' })

const outFile = path.join(releaseDir, `${outputName}.exe`)
console.log(`\nInstaller created: ${outFile}`)
console.log(`Size: ${(fs.statSync(outFile).size / 1024 / 1024).toFixed(1)} MB`)
